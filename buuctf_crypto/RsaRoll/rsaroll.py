import gmpy2

# data.txt中的{920139713,19}，第一个括号里第一个是n，第二个是e
# 下面是c，每行代表一个c
# 分解 n 为 p、q
# 920139713 = 18443 * 49891

e = 19
n = 920139713
p = 18443
q = 49891
data = [704796792, 752211152, 274704164, 18414022, 368270835, 483295235, 263072905, 459788476, 483295235, 459788476, 663551792, 475206804, 459788476, 428313374, 475206804, 459788476, 425392137, 704796792, 458265677, 341524652, 483295235, 534149509, 425392137, 428313374, 425392137, 341524652, 458265677, 263072905, 483295235, 828509797, 341524652, 425392137, 475206804, 428313374, 483295235, 475206804, 459788476, 306220148]

phi = (p - 1) * (q - 1)
d = gmpy2.invert(e, phi)
res = ''
for i in data:
    m_tmp = gmpy2.powmod(i, d, n)
    res += chr(m_tmp)
print(res)
